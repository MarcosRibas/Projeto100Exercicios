/*
Ex046 Faça um programa que mostre na tela uma contagem regressiva para o 
estouro de fogos de artifício, indo de 10 até 0.
 */
package ex046;

public class Ex046 {


    public static void main(String[] args) {
        for (int c = 10; c>=0;c--)
            System.out.println(c);
        System.out.println("Feliz ano novo!");      


    }
    
}
